/**
 * Check if a tile position is walkable
 * @param x - tile x coordinate
 * @param y - tile y coordinate
 * @returns true if walkable, false if collision
 */
export function isTileWalkable(x: number, y: number): boolean {
    // Check bounds
    if (x < 0 || x >= MAP_WIDTH || y < 0 || y >= MAP_HEIGHT) {
        return false;
    }
    
    // Check collision map
    return COLLISION_MAP[y][x] === 0;
}

/**
 * Convert pixel coordinates to tile coordinates
 * @param pixelX - pixel x coordinate
 * @param pixelY - pixel y coordinate
 * @returns object with tileX and tileY
 */
export function pixelToTile(pixelX: number, pixelY: number): { tileX: number; tileY: number } {
    return {
        tileX: Math.floor(pixelX / TILE_SIZE),
        tileY: Math.floor(pixelY / TILE_SIZE)
    };
}

/**
 * Convert tile coordinates to pixel coordinates (center of tile)
 * @param tileX - tile x coordinate
 * @param tileY - tile y coordinate
 * @returns object with pixelX and pixelY
 */
export function tileToPixel(tileX: number, tileY: number): { pixelX: number; pixelY: number } {
    return {
        pixelX: tileX * TILE_SIZE + TILE_SIZE / 2,
        pixelY: tileY * TILE_SIZE + TILE_SIZE / 2
    };
} 